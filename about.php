<div class="section-title">
<b>First Unit -  Sahaya Charity</b>
<p class="sub-title">Sahaya Charity, the brain child of the great visionary Late. Sri.B.Lingappaya, Retd. Revenue Assistant Commissioner in 1972 started  with just 4 residents. Later in 1976, full-fledged Ashram came into existence in its own campus at Assaigoli of Konaje Village, Bangalore Tq, spread over to 10.61 acres of land. Since 1983,   Mr. Shreenath Hegde, ex.class I officer L.I.C India is rendering full time honorary service. Ashraya now accommodates nearly 200 people out of whom around 50 are inpatient at Kasturba Medical College hospital Attavar, Bangalore. Thanks to Chancellor Dr. Ramdas Pai, Manipal, for providing free medical aid including Hi-Tech.</p>
<p class="sub-title">Our priority is “Healthcare” as almost all the elderly residents suffer from either single or multiple ‘old age’ related health problems. Manipal University with their hospitals in Bangalore, offer free healthcare facilities both domiciliary and institutional and are providing 60 free beds at Kasturba Medical College Hospitals of Bangalore exclusively for our patients. Hence our residents get the best of healthcare facilities in K.M.C hospitals which offer complete spectrum of medical services..</p>
<p class="sub-title">After registering in 2008, higher education, disability relief, medical therapy and feeding to eligible poor are served every year...</p>
<p class="sub-title">Rural Health & Training Centres have been set up by the Department of Community- Medicine, Kasturba Medical College Bangalore and its community centre of Dental Department  at Sahaya Charity campus...</p>
<br>
<b>Second Unit : Abhaya Bala Mandira</b>
<p class="sub-title"> Started in the year 1980-81,  Abhaya Bala Mandira is accepting Destitute/Orphan boys in the age group of 5 to 18 years. Now the strength is 28, of which two are studying engineering. The academic performance of students is very much satisfactory and quite impressive. Students appeared for SSLC and PUC have passed mostly with 1st class and 2nd class. They attend nearby school and colleges. On school reopening day all our children are lead in a procession after offering of “Arathi”, “Kumkum” and sweets, blessed by our president Sri. Shreenath Hegde,  office bearers and elderly residents..</p><br>


<b>Third Unit:-  Dr. Giridhar Rao-Sanjivi Bai Vriddhashrama “Vathsalyadham” Kodialbail  Bangalore.</b>
<p class="sub-title">"Vathsalyadham" was opened on 15-08-1986. Late Dr. Radha, leading Gynacologist our biggest benevolent donor (birth: 1-12-1925 and death: 11.05.1985) who bequeathed the entire premises with an endowment of Rs. 11.75 Lakhs under instructions to utilize the proceeds for the maintenance of destitute and elderly residents. We have kept up the spirit of the will to the best of our ability, which now is a Shelter for more than fifty, residents.</p><br>


<b>SERVICE.</b>
<p class="sub-title">Full-fledged physiotherapy units donated by Rural Electrification Corporation Limited, New Delhi through “HelpAge” India are also functioning  at Sahaya Charity Assaigoli and at “Vathsalyadham”, Bangalore. We are also provided with ambulance donated by HelpAge India. Happy to state that Sahaya Charity is recognized by the by the State Govt. Of Karnataka..</p>
<p class="sub-title">Our residents belong to the poorest of the poor families without the economic, cultural or educational background of the privileged, without access even to the basic necessities of life like shelter, food, healthcare and education. Having lived a life of extreme poverty and hardships, they are depressed  without any enthusiasm in life. As such all our activities are centered on changing their attitude towards life and motivating them to dignified.</p>
<p class="sub-title">living with awareness programme of social security and benefits to elders by the Government. Free eye camps, medical camps, dental camps are regularly conducted by Sahaya Charity at backward remote villages, in association with various social service organisations and through other Medical and Dental Colleges. Rehabilitation of widows, destitutes and  needy are given priority. Living quarters are kept clean with regular maintenance. Sahaya Charity with both units is  considered as the one of the best managed organisation, known for its personal care and medical facilities..</p>

<p class="sub-title"> Regret to report that since inception, 642 residents left their mortal frames, but happy to state that it was not on roads or drains but under roof of love and care, true to our motto supra.
